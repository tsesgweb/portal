            <p style="text-align: justify; ">Installed this Tuesday night as chief of the Superior Electoral Court (TSE) for the next months, Justice Luiz Fux and Justice Rosa Weber took the post of President and Vice-president of the Court, respectively. Justice Luiz Fux
                will be TSE’s president until August 15, when he will end his second biennium as effective Justice and will leave the Court. Fux will be succeeded by Justice Rosa Weber.</p>
            <p style="text-align: justify; ">Since his installation as an effective Justice of the TSE on August 2014, Justice Luiz Fux has been standing out for his valorization of the Clean Record Law (Declaratory Statute n. 135/2010), as an instrument to “separate the
                sheep from the goats” in politics; for his defense of freedom of speech; for his firm acting alongside the Electoral Justice in order to correct and punish the excesses practiced by candidates and parties; and for his search
                to define a reasonable term, for the parties, to make the provisional comissions into definite, in the name of the internal democracy of the parties. Justice Luiz Fux is the assignee of the 2018 Elections Resolutions.</p>
            <p style="text-align: justify; ">Before elaborating the Resolutions with the help of the Justices of the Court and Counsellours, Justice Luiz Fux chaired, last November, the cycle of public audiences in which citizens, which are representatives of the Department
                of Justice, civil entities and political parties; presented suggestions for the improvement of drafts about the 2018 General Elections.</p>
            <p style="text-align: justify; ">As an assignee, the current vice-president of TSE has taken to the Court, on the beginning of December 2017, ten resolutions about the rules that will come into force on this years election. All of them were approved in an administrative
                session. The TSE has until this year’s March 5 to dispatch all the instructions about the election dispute.&nbsp; This norm consists on Art. 105 of the Elections Law (Law n. 9,504/1997).</p>
            <p style="text-align: justify; ">At the time, Justice Luiz Fux informed that all the approved resolutions can be a subject of adjustments and improvements until the end of the term. He communicated that an audience will be held and a specific resolution about
                the printed vote will be elaborated, because there are still budgetary and technological issues about the theme.</p>


        </div>
        <div role="tabpanel" class="tab-pane" id="ancora-2">
            <p style="text-align: justify; ">“The printed vote will be a subject for not only one proper audience but also of a proper resolution, because it is maybe one of the most significant inovattions of this election.&nbsp;&nbsp; We will need to have the consent of
                the parties, of other members of the (Public) Power and also budgetary compatibility. Therefore, many are the issues that we want to debate in order to meet the collective yearnings,” Fux said.</p>
            <p style="text-align: justify; ">At the judgment for the parties’ rendering of accounts in 2011, Justice Luiz Fux made a severe alert to the coalitions, despite voting for the approval, with reservations, of the accounts of some coalitions with bases on TSE’s
                jurisprudence that ruled at the time (considering the percentage of a maximum of 10% of irregularities for the approval with reservations).</p>
            <p style="text-align: justify; ">At the occasion, he highlighted that, if the rendering of accounts referred to the current period, he would have voted for the disapproval. “These accounts are from 2011, so, begging from now, the gravity will be insurmountable
                and therefore the parties should be prepared.”</p>
            <p style="text-align: justify; ">“Today, we watch a frame of ethical degeneration of politics, which has the approval of the accounts, by the Electoral Justice, as a support for the defense.&nbsp; Thus, it is a serious issue and I think that the Electoral Justice
                has to tighten the control.&nbsp; During the period were I will be able to exercise the presidency of the Court, I will effectively have a vision that is strictly rigorous with the rendering of accounts, considering the gravity
                of the facts; and we will try to build here a jurisprudence that is based on this past facts, which are being revealed today and that have the approval of the accounts, by the TSE, as a shield,” added the Justice at the time
                of the judgment.</p>
            <p style="text-align: justify; ">Justice Luiz Fux has been an effective member of the Court since August 14, 2014, being renewed in the post two years after.&nbsp; He took possession as vice-president of the TSE on May 12, 2016.&nbsp; He acted as substitute Justice
                of the court from May 2011 until he was installed as full Justice. Fux took possession as Justice of the Supreme Federal Court (STF) on March 3, 2011.</p>
            <p style="text-align: justify; ">Luiz Fux has a doctorate degree on Civil Procedure Law by the State University of Rio de Janeiro (UERJ) since 2009. He was a Justice of the Superior Court of Justice (STJ) from 2001 to 2011 and Associate Justice of the state Judicial
                Branch of Rio de Janeiro (TJ/RJ) from 1997 to 2011.</p>
            <p style="text-align: justify; ">Natural of Rio de Janeiro (RJ), the Justice chaired the Commission of Jurists entitled with the elaboration of the bill that resulted on the new Civil Procedure Code (CPC), which is already in force.&nbsp; He integrates the Academia
                Brasileira de Letras Jurídicas (Brazilian Judicial Letters Academy), he is full professor of Civil Procedure at UERJ”s College of Law and author of many works from Civil Procedure and Constitutional Law, amongst other posts
                and qualifications.</p>

        </div>
        <div role="tabpanel" class="tab-pane" id="ancora-3">
            <p style="text-align: justify; ">One of the decisive votes of Justice Rosa Weber at the Court was in the post of assignee in the rendering of account of Partido dos Trabalhadores (PT - Labour Party) in 2011.&nbsp; Based on her vote, the Justices partially disapproved
                PT’s rendering of account and determined that the party should return to the Public Treasure a value of R$ 5,623,893.50.</p>
            <p style="text-align: justify; ">Born in Porto Alegre (RS), Justice Rosa Weber graduated in the College of Law of the Federal University of Rio Grande do Sul (Ufrgs). She was a Labour Court from 1976 to 1991. She was a judge of the Regional Labour Court of the
                4
                <sup>th</sup> Region from 1991 to 2006.</p>
            <p style="text-align: justify; ">Rosa Weber was Justice of the Superior Court of&nbsp; Labour (TST) beginning in 2006.&nbsp; She became Justice of the Supreme Federal Court&nbsp; (STF) on December 19, 2011. She took possession as effective Justice of the TSE on
                May 24, 2016.&nbsp; Her installation as substitute Justice of the Electoral Court happened on June 12, 2012.</p>

        </div>
        <div role="tabpanel" class="tab-pane" id="ancora-4">
            <p style="text-align: justify; ">The TSE is composed by, at least, seven Justices.&nbsp; Three Justices are from the STF, one of which will be the President of the Court, two Justices from the Superior Court of Justice (STJ), one of which will be the General Chair
                of Judicial Council of the Electoral Justice, and two jurists from the advocacy class, which are nominated by the President of the Republic.</p>
