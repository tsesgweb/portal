        <div role="tabpanel" class="tab-pane" id="ancora-2">
            <h3>Missão do TSE</h3>
            <p class="textosinternos">Garantir a legitimidade do processo eleitoral e o livre exercício do direito de votar e ser votado, a fim de fortalecer a democracia.</p>
            <h3>Visão</h3>
            <p class="textosinternos">Consolidar a credibilidade da Justiça Eleitoral, especialmente quanto à efetividade, à transparência e à segurança.</p>
            <h3>Valores</h3>
            <p><strong>Coerência</strong>: alinhamento entre discurso e prática.</p>
            <p><strong>Comprometimento</strong>: atuação com dedicação, empenho e envolvimento em suas atividades.</p>
            <p><strong>Respeito</strong>: reconhecimento e aceitação das diferenças entre as pessoas.</p>
            <p><strong>Inovação</strong>: estímulo à criatividade e à busca de soluções diferenciadas.</p>
            <p><strong>Flexibilidade</strong>: atitude de abertura permanente para compreender a necessidade de mudanças adotando medidas para promovê-las.</p>
            <p><strong>Integração</strong>: compartilhamento de experiências, conhecimentos e ações que conduzam à formação de equipes orientadas para resultados comuns.</p>
            <p><strong>Reconhecimento</strong>: adoção de práticas de estímulo e valorização das contribuições individuais e de grupos que conduzam ao cumprimento da missão do TSE.</p>
            <p><strong>Transparência</strong>: garantia do acesso às informações, ações e decisões institucionais.</p>
            <p><strong>Ética</strong>: atuação sob os princípios da honestidade, lealdade e dignidade.</p>
