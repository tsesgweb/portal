        <div role="tabpanel" class="tab-pane active" id="ancora-1">
            <p>The Superior Electoral Court (TSE), highest body of the Electoral Justice, plays a fundamental role towards building and exercising democracy in Brazil. Its main competencies are established by the Federal Constitution and by the Electoral Code (Law No. 4,737, dated 07/15/1965).</p>
            <p>The TSE has joint action with the Regional Electoral Courts (TREs), which are directly responsible for the administration of the electoral process in states and municipalities.</p>
            <p>The Court is composed of seven Justices: three from the Supreme Federal Court, two from the Superior Court of Justice and two representatives from the class of - jurists - lawyers with remarkable legal expertise and suitability.</p>
            <p>Each Justice is elected for a mandate of two years (biennium), and is prohibited of renewing it after two consecutive biennia. The rotation of the Justices in the framework of the Electoral Justice aims at maintaining the apolitical character of the courts, in order to guarantee the isonomy of the elections.</p>
            <p>The TSE is chaired by a Justice from the Supreme Federal Court (STF). Currently, Justice Luiz Fux chairs the Court.</p>
            <p>The Office of Electoral Inspector General is held by one of the Justices of the Superior Court of Justice (STJ). It is currently occupied by Napoleão Nunes Maia Filho.</p>
            <p>Attached to the Court’s Presidency are the General Secretariat of the Presidency, which is formed by the Judicial Secretariat and six advisories, and the Registry of the Court, which is formed by four advisories and six secretariats.</p>
            <p>There is also the Electoral Judiciary School, which aims to carry out the training, updating and continuous or eventual specialization of the Electoral Justice magistrates and those interested in Electoral Law, who are appointed by public bodies and public and private entities.</p>
