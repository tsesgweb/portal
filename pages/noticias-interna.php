                <div role="tabpanel" class="tab-pane active" id="ancora-1">
                    <p>A Lei dos Partidos Políticos (Lei nº 9.096/1995) completa 24 anos nesta quinta-feira (19). Ao
                        longo desse tempo, embora tenha passado por diversas reformas normativas, a lei manteve seu
                        papel fundamental de assegurar a representatividade e a autonomia das agremiações, prerrogativas
                        garantidas pela Constituição Federal de 1988.</p>
                    <p>Sancionada em 19 de setembro de 1995, a legislação substituiu a Lei Orgânica dos Partidos (Lei n°
                        5.682/1971), remanescente ainda do período do regime militar no Brasil. Assim, as agremiações
                        passaram a ter autonomia, e a natureza jurídica das legendas passou a ser privada, entre outras
                        alterações.</p>
                    <p>O artigo 17 da Constituição Federal estabelece como livre a criação, a fusão, a incorporação e a
                        extinção de partidos políticos, resguardados a soberania nacional, o regime democrático, o
                        pluripartidarismo e os direitos fundamentais da pessoa humana. O texto constitucional exige que
                        os partidos tenham caráter nacional, prestem contas à Justiça Eleitoral e tenham funcionamento
                        parlamentar de acordo com a lei vigente.</p>
                    <p>De acordo com o secretário judiciário do TSE, Fernando Alencastro, a Lei nº 9.096 assegura às
                        legendas autonomia para definir sua estrutura interna, organização e funcionamento, bem como
                        para adotar os critérios de escolha e o regime de suas coligações eleitorais, sem a
                        obrigatoriedade de vinculação entre as candidaturas em âmbito nacional, estadual, distrital ou
                        municipal.</p>
                    <p>Alencastro explica que a Lei dos Partidos Políticos também regulamenta outros importantes pontos,
                        tais como: uso e distribuição do Fundo Partidário; filiação e fidelidade partidária; prestação
                        de contas; e acesso gratuito ao rádio e à TV. “Em 2009, ocorreu uma mudança significativa no
                        artigo 37 da Lei, fazendo com que a prestação de contas das agremiações à Justiça Eleitoral
                        passasse de procedimento administrativo para jurisdicional. Isso fez com que se ampliasse a
                        possibilidade de defesa, com a participação de advogados na prestação de contas”, revelou.</p>
                    <p>Confira, a seguir, alguns dos principais pontos e mudanças ocorridas ao longo da vigência da
                        norma:</p>
                    <h3 id="titulo-1">Criação e registro dos partidos</h3>
                    <p>A Lei 9.096/1995 estabelece regras gerais para criação e registro dos partidos. A Resolução do
                        TSE nº 23.282/2010, por sua vez, detalha o que é necessário para se criar uma nova legenda. Quem
                        defere ou não a criação do partido é o TSE. Até a protocolização do pedido neste Tribunal, o
                        partido em formação deve cumprir exigências, tais como: a elaboração do programa e do estatuto
                        por pelo menos 101 fundadores; o apoio de eleitores correspondente a, no mínimo, 0,5% dos votos
                        dados na última eleição geral para a Câmara dos Deputados, distribuído por um terço ou mais dos
                        Estados; e a certidão de apoio dos eleitores, emitida por cartórios eleitorais.</p>
                    <h3 id="titulo-2">Fundo Partidário</h3>
                    <p>A Lei 9.096/1995 estabelece regras gerais para criação e registro dos partidos. A Resolução do
                        TSE nº 23.282/2010, por sua vez, detalha o que é necessário para se criar uma nova legenda. Quem
                        defere ou não a criação do partido é o TSE. Até a protocolização do pedido neste Tribunal, o
                        partido em formação deve cumprir exigências, tais como: a elaboração do programa e do estatuto
                        por pelo menos 101 fundadores; o apoio de eleitores correspondente a, no mínimo, 0,5% dos votos
                        dados na última eleição geral para a Câmara dos Deputados, distribuído por um terço ou mais dos
                        Estados; e a certidão de apoio dos eleitores, emitida por cartórios eleitorais.</p>
                    <h3 id="titulo-3">Cláusula de desempenho</h3>
                    <p>De acordo com o artigo 41-A da Lei dos Partidos Políticos, 5% do total do Fundo Partidário deve
                        ser distribuído, em partes iguais, a todas as legendas, e os outros 95%, na proporção dos votos
                        obtidos pelas agremiações na última eleição geral para a Câmara dos Deputados. Contudo, a Emenda
                        Constitucional (EC) nº 97/2017 trouxe mudanças ao estabelecer que apenas as siglas que cumprirem
                        os requisitos de acesso estipulados pela cláusula de desempenho terão direito aos valores do
                        Fundo.</p>
                    <p>Atualmente, recebem os recursos do Fundo as agremiações que atingiram no pleito de 2018 pelo
                        menos 1,5% dos votos válidos nas eleições para a Câmara dos Deputados, distribuídos em pelo
                        menos um terço das unidades federativas, com no mínimo 1% de votos válidos em cada uma delas.
                        Ou, ainda, as siglas que elegeram ao menos nove deputados federais distribuídos em pelo menos um
                        terço das 27 unidades da Federação. Esse cálculo será válido até 2022, já que a EC prevê o
                        ajuste escalonado do percentual até o ano de 2030.</p>
                    <p>A cláusula de desempenho também estabelece novos critérios para o tempo de propaganda eleitoral
                        gratuita no rádio e na televisão. O desempenho eleitoral exigido das legendas partidárias será
                        aplicado de forma gradual e alcançará seu ápice nas eleições de 2030, conforme previsto na EC nº
                        97/2017.</p>
                    <h3 id="titulo-4">Coligações</h3>
                    <p>A EC nº 97 também vedou, a partir de 2020, a celebração de coligações nas eleições proporcionais
                        para a Câmara dos Deputados, Câmara Legislativa, assembleias legislativas e câmaras municipais.
                        Um dos principais reflexos da mudança se dará no ato do pedido de registro de candidaturas à
                        Justiça Eleitoral, especialmente porque, com o fim das coligações, cada partido deverá,
                        individualmente, indicar o mínimo de 30% de mulheres filiadas para concorrer no pleito.</p>
                    <h3 id="titulo-5">Propaganda Partidária e Fundo Especial</h3>
                    <p>Em 2017, a Lei nº 13.488 extinguiu a propaganda partidária no rádio e na TV. Outra Lei, a de nº
                        13.487, do mesmo ano, instituiu o Fundo Especial de Financiamento de Campanha (FEFC). Com o fim
                        da propaganda partidária, os valores de compensação fiscal das emissoras de rádio e televisão
                        passaram para a constituição do FEFC.</p>
                    <p>O Fundo concentra recursos que serão utilizados pelos partidos políticos para financiar as
                        campanhas eleitorais de seus candidatos. Com a proibição de doações por parte de pessoas
                        jurídicas, estabelecida pelo Supremo Tribunal Federal (STF), o FEFC tornou-se uma das principais
                        fontes de receita para a realização das campanhas.</p>
                    <h3 id="titulo-6">Candidatura de mulheres</h3>
                    <p>De acordo com o secretário judiciário do TSE, Fernando Alencastro, a partir de 2020, as legendas
                        deverão encaminhar à Justiça Eleitoral, juntamente com o Demonstrativo de Regularidade de Atos
                        Partidários (DRAP), a lista de candidatas que concorrerão no pleito, respeitando-se o percentual
                        mínimo de 30% e o máximo de 70% para candidaturas de cada sexo. A regra está prevista no artigo
                        10, parágrafo 3º da Lei nº 9.504/1997 (Lei das Eleições).</p>

