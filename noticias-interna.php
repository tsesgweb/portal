            <p>A emissão da Identidade Digital deverá ter início a partir do segundo semestre deste ano para todos os cidadãos que se cadastrarem no programa Identificação Civil Nacional (ICN). Sob a responsabilidade do Tribunal Superior Eleitoral (TSE),
                serão coletadas as informações biométricas e biográficas de brasileiros que ainda não constam da base de dados do Tribunal. O novo documento vai garantir que ninguém tente se passar por outra pessoa na hora da identificação em qualquer
                instituição pública ou privada.</p>
            <p>O TSE será responsável por gerir o banco de dados e emitir a Identidade Digital ou, ainda, certificar outros órgãos para a expedição do documento. O Tribunal conta com <em>expertise</em> e infraestrutura capazes para coletar a biometria
                de todos os cidadãos brasileiros. Além da coleta, a Corte Eleitoral será responsável pelo armazenamento e pelo possível compartilhamento dessas informações com outros órgãos.</p>
            <p>Confira abaixo as respostas para algumas dúvidas comuns acerca da Identidade Digital e do programa ICN.</p>
            <p><b>A Identidade Digital substituirá o documento de identidade atual impresso?</b></p>
            <p>Sim. No entanto, num primeiro momento, quem não dispuser de smartphone ou tablet poderá ter o número de inscrição no programa ICN incluído no documento de identidade físico. A Identidade Digital, conhecida na legislação como Documento
                Nacional de Identidade (DNI), contará com um número de identificação – interno e individual para cada cidadão – de nove dígitos, e também exibirá a numeração de outros documentos, como a do CPF.</p>
            <p><b>A emissão do documento será gratuita?</b></p>
            <p>Sim.</p>
            <p><b>O que é o programa de Identificação Civil Nacional (ICN)?</b></p>
            <p>Criado pela <a href="http://www.planalto.gov.br/ccivil_03/_ato2015-2018/2017/lei/l13444.htm" target="_blank">Lei n° 13.444/2017</a>, o programa de Identificação Civil Nacional (ICN) está sob a responsabilidade do TSE. O objetivo é individualizar
                os brasileiros para que sejam identificados com segurança e facilidade em suas relações públicas e particulares.</p>
            <p><b>A nova identidade será mais segura?</b></p>
            <p>Sim<b>. </b>A identidade ainda contará com dispositivos de segurança de verificação que os documentos comuns não possuem. Isso só será possível porque o DNI utilizará dados biométricos que são únicos em cada indivíduo. A expedição da identidade
                oficial dos brasileiros obedecerá a padrões, procedimentos e elementos de segurança.</p>
            <p><b>Qual a diferença entre dados biográficos e dados biométricos?</b></p>
            <p>Os dados biográficos são nome completo, gênero, estado civil, cor/raça, data de nascimento, nacionalidade/naturalidade e ocupação. Já as informações biométricas são as impressões digitais e da face e a assinatura dos indivíduos.</p>
            <p><b>Além do componente biométrico, a Identidade Digital contará com quais outros itens de segurança?</b></p>
            <p>O documento será gerado e poderá ser autenticado mediante processo de verificação de chaves de segurança em um servidor protegido. Os dados que aparecerão nos dispositivos móveis aos cidadãos serão criptografados, o que também aumenta
                a proteção da informação. Esses dados em “códigos” só podem ser corretamente lidos por quem possui uma espécie de “chave” para o segredo.</p>
            <p>O aplicativo apresentará, ainda, um QR Code, que será criado de forma dinâmica a cada novo acesso, mantendo os dados de validação vinculados à data e à hora de sua geração. O DNI também mostrará no canto superior direito, como marca d'água,
                um código de verificação contendo 20 caracteres, precedido da data e da hora em que foi gerado.</p>
            <p><b>O programa ICN vai aproveitar os dados dos eleitores com biometrias cadastradas?</b></p>
            <p>Sim. Atualmente, cerca de 90 milhões de pessoas já tiveram os dados biométricos coletados pela Justiça Eleitoral em todo o país.</p>
            <p><b>Outros órgãos que tratam da identificação dos brasileiros participam do projeto?</b></p>
            <p>O TSE atuará em parcerias com outros órgãos que deverão seguir o mesmo padrão de identificação biométrica utilizado pela Justiça Eleitoral, que abarca a coleta das digitais dos dez dedos da mão e a captura da face do cidadão.</p>
            <p>O objetivo é que possa existir o intercâmbio das informações desses órgãos com o TSE, que serão verificadas e disponibilizadas no programa ICN. É o caso dos institutos de identificação dos Estados.</p>
