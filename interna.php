                <p>A Justiça Eleitoral (JE), sempre preocupada com o aprimoramento contínuo dos pleitos eleitorais, iniciou, em 2008, mais uma etapa da modernização do processo eleitoral com a implantação do Programa de Identificação Biométrica, utilizando-se
                    da tecnologia para reconhecimento individual do eleitor, baseada em dados biométricos (impressões digitais).</p>
                <p>Àquela época, a sistemática de identificação do eleitor, etapa preliminar e fundamental do processo eleitoral, mereceu a atenção dessa Justiça especializada principalmente em dois aspectos: no processo de identificação do eleitor na seção
                    eleitoral e nas técnicas que detectam a duplicidade das inscrições no Cadastro Eleitoral.</p>
                <p>Quanto ao primeiro aspecto, o intuito do projeto é a evolução do processo de identificação no momento em que o eleitor se apresenta para votar, trazendo para essa etapa mecanismos tecnológicos, como os dados biométricos. Dessa forma, fortalecem-se
                    os critérios de garantia de habilitação do eleitor para votar, anteriormente restritos à conferência de documentos de identificação – procedimento manual que dava margem a falhas – e ao controle por parte de delegados e fiscais de
                    partidos, cuja presença não depende de ações da JE.</p>
